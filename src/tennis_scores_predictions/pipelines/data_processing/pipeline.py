from kedro.pipeline import Pipeline, node

from .nodes import (
    add_round_index,
    calculate_diff_features,
    compute_elo_node,
    drop_feature_metadata_columns,
    drop_match_identifiers,
    drop_missing_demographics,
    drop_missing_rank,
    drop_missing_rank_points,
    drop_seed_columns,
    encode_categorical,
    filter_active_players_node,
    impute_player_heights,
    impute_winner_loser_entry,
    replace_string_nan_with_na,
    select_columns_before_match,
    transform_names_node,
)


def create_pipeline(**kwargs) -> Pipeline:
    return Pipeline(
        [
            node(
                select_columns_before_match,
                inputs="combined_matches",
                outputs="matches_column_selected",
            ),
            node(
                filter_active_players_node,
                inputs="matches_column_selected",
                outputs="matches_active_players",
            ),
            node(
                replace_string_nan_with_na,
                inputs="matches_active_players",
                outputs="matches_nan_replaced",
            ),
            node(
                impute_winner_loser_entry,
                inputs="matches_nan_replaced",
                outputs="matches_entry_imputed",
            ),
            node(
                drop_seed_columns,
                inputs="matches_entry_imputed",
                outputs="matches_seeds_dropped",
            ),
            node(
                impute_player_heights,
                inputs="matches_seeds_dropped",
                outputs="matches_height_imputed",
            ),
            node(
                drop_missing_rank_points,
                inputs="matches_height_imputed",
                outputs="matches_rank_points_complete",
            ),
            node(
                drop_missing_rank,
                inputs="matches_rank_points_complete",
                outputs="matches_rank_complete",
            ),
            node(
                drop_missing_demographics,
                inputs="matches_rank_complete",
                outputs="matches_demo_complete",
            ),
            node(
                drop_match_identifiers,
                inputs="matches_demo_complete",
                outputs="matches_cleaned",
            ),
            node(
                transform_names_node,
                inputs=dict(
                    df="matches_cleaned",
                    params="params:feature_engineering",
                ),
                outputs="matches_symmetric",
            ),
            node(
                add_round_index,
                inputs="matches_symmetric",
                outputs="matches_round_index",
            ),
            node(
                compute_elo_node,
                inputs=dict(
                    df="matches_round_index",
                    params="params:feature_engineering",
                ),
                outputs="matches_elo",
            ),
            node(
                calculate_diff_features,
                inputs="matches_elo",
                outputs="matches_diff_features",
            ),
            node(
                drop_feature_metadata_columns,
                inputs="matches_diff_features",
                outputs="matches_pre_encoded",
            ),
            node(
                encode_categorical,
                inputs="matches_pre_encoded",
                outputs="matches_features",
            ),
        ]
    )
